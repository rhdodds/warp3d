c     ****************************************************************
c     *                                                              *
c     *                   subroutine mm10_mrr_GH                     *
c     *                                                              *
c     *                       written by : tjt                       *
c     *                                                              *
c     *                   last modified: 4/6/2016 tjt                *
c     *                                                              *
c     *   drive slection/filling of interaction matrices G & H       *
c     *   for parallel and forest dislocations                       *
c     *                                                              *
c     *                                                              *
c     ****************************************************************
c
      subroutine mm10_mrr_GH(s_type,num_hard,G,H,i,c)
      implicit none
c
      integer :: s_type, num_hard, i, c
      double precision, dimension(num_hard,num_hard) ::  G, H
c
      if( s_type .eq. 1 ) then ! FCC
         call mm10_mrr_GHfcc( G,H )
      elseif( s_type .eq. 2 ) then ! BCC
         call mm10_mrr_GHbcc( G,H )
      elseif( s_type .eq. 6 ) then ! FCC12
         call mm10_mrr_GHfcc12( G,H )
      elseif( s_type .eq. 7 ) then ! BCC12
         call mm10_mrr_GHbcc12( G,H )
      elseif( s_type .eq. 8 ) then ! BCC48
         call mm10_mrr_GHbcc48( G,H )
      else ! calculate manually
         write(*,*) 'cannot use manual interaction G,H matrices'
         write(*,*) 'routine mm10_mrr_GH. terminate job'
         call die_gracefully
      endif
c
      return
      end subroutine

